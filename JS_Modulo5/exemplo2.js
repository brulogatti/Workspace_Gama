//Vamos criar variaveis usando let, const e var, atribuir strings e vamos concatenar e interpolar.
let palavra1 = "Bruna"
const palavra2 = "gosta"
var palavra3 = "de programar"

//Concatenação
console.log(palavra1 + " " + palavra2 + " " + palavra3)
//Interpolação
console.log('%s %s %s', palavra1, palavra2, palavra3)